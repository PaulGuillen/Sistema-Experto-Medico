   public String dameUbigeo(String ubigeo) {
      String UBIGEO = "";
      if (ubigeo != null) {
         int d1 = Integer.parseInt(ubigeo.substring(0, 2)) - 1;
         int d2 = Integer.parseInt(ubigeo.substring(2, 4)) - 1;
         int d3 = Integer.parseInt(ubigeo.substring(4)) - 1;
         cargaDepa();
         cargaProv();
         cargaDist();
         UBIGEO = "\n\tDpto: " + Depa[d1] + "\n\tProv: " + Prov[d1][d2] + "\n\tDist: " + Dist[d1][d2][d3];
      } else {
         UBIGEO = "SIN DATOS";
      }
      return UBIGEO;
   }

