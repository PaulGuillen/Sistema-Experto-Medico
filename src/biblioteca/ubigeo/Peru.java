   public void cargaCombo(JComboBox dist, JComboBox prov, JComboBox depa) {
      if (prov.getSelectedIndex() < 1) {
         dist.removeAllItems();
      } else {
         cargaDist();
         int ii = depa.getSelectedIndex() - 1;
         int jj = prov.getSelectedIndex() - 1;
         dist.removeAllItems();
         dist.addItem("Elija Distrito");
         for (int k = 0; k < D3; k++) {
            if (Dist[ii][jj][k].compareTo("") != 0) {
               dist.addItem(Dist[ii][jj][k]);
            }
         }
      }
   }

